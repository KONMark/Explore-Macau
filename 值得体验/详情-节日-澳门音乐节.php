        <p style="line-height:40px;">澳门国际音乐节为每年在澳门举办一次的国际音乐盛事，创办于1987年。
        1992年，该项盛事达到了国际化目标，澳门更被公认为东南亚地区举办以演出高雅音乐为主的音乐节之城市。<br>
        多年来参加过音乐节的艺术家约有200人，参与团体约50个，音乐会数目共136场。历届音乐节邀请国内外著名音乐团体参加，如：中央交响乐团，中国广播交响乐团，奥斯堡乐团，里斯本管乐队等，澳门本地音乐团体也参加演出。
        每届音乐节均有不少世界著名指挥家，歌唱家参加演出。音乐演出安排在综艺馆，澳督府，大三巴，东亚大学（澳门大学）澳门文化中心等地举行。</p>
